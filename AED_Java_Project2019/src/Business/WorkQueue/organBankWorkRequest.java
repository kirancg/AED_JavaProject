package Business.WorkQueue;

import java.util.Date;

/**
 *
 * @author Charan
 */
public class organBankWorkRequest extends WorkRequest{
    
    private String name;
    private Date dateOfBirth;
    private String sex;
    private String address;
    private String city;
    private String state;
    private String age;
    private String mobileNumber;
    private String emailOfUserToOrgan;

    public String getEmailOfUserToOrgan() {
        return emailOfUserToOrgan;
    }

    public void setEmailOfUserToOrgan(String emailOfUserToOrgan) {
        this.emailOfUserToOrgan = emailOfUserToOrgan;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDateOfBirth() {
        return dateOfBirth;
    }

    public void setDateOfBirth(Date dateOfBirth) {
        this.dateOfBirth = dateOfBirth;
    }



    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }
    
    

}
